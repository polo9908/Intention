/**
 * mcpGenerator.ts — Generate a complete, ready-to-run MCP server
 * (Model Context Protocol) from an AgentSpec.
 *
 * The generated server is a self-contained TypeScript/Node.js file that:
 *   • Accepts tool calls via stdio (MCP transport)
 *   • Validates inputs against the spec constraints
 *   • Calls Claude with the spec's system prompt and config
 *   • Applies escalation logic based on capability and escalationRate
 *   • Handles errors and returns structured MCP responses
 *
 * Reference: https://modelcontextprotocol.io/docs/spec
 */

import type { AgentSpec } from "@/types";

// ---------------------------------------------------------------------------
// Output type
// ---------------------------------------------------------------------------

export interface McpGeneratorResult {
  /** The full TypeScript source code for the MCP server */
  serverCode: string;
  /** package.json content for the generated server */
  packageJson: string;
  /** README content */
  readme: string;
  /** Suggested file name */
  filename: string;
}

// ---------------------------------------------------------------------------
// Helpers
// ---------------------------------------------------------------------------

function jsString(s: string): string {
  return JSON.stringify(s);
}

function indent(code: string, spaces: number): string {
  const pad = " ".repeat(spaces);
  return code
    .split("\n")
    .map((l) => (l.trim() === "" ? "" : pad + l))
    .join("\n");
}

// ---------------------------------------------------------------------------
// Package.json generator
// ---------------------------------------------------------------------------

function generatePackageJson(spec: AgentSpec): string {
  const slug = spec.name.toLowerCase().replace(/[^a-z0-9]+/g, "-").slice(0, 40);
  return JSON.stringify(
    {
      name:    `@contextlayer/${slug}-mcp`,
      version: `1.0.${spec.version}`,
      description: spec.description || `MCP server for ${spec.name}`,
      main:    "server.js",
      scripts: {
        start:   "node server.js",
        build:   "tsc server.ts --outDir . --target ES2022 --module commonjs",
        dev:     "ts-node server.ts",
      },
      dependencies: {
        "@anthropic-ai/sdk": "^0.39.0",
        "@modelcontextprotocol/sdk": "^1.0.0",
      },
      devDependencies: {
        typescript:  "^5.7.0",
        "ts-node":   "^10.9.0",
        "@types/node": "^22.0.0",
      },
      engines: { node: ">=18.0.0" },
      license: "MIT",
    },
    null,
    2,
  );
}

